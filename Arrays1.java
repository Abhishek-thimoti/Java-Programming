public class Arrays1 {
    public static void main(String[] args){
        int arr[] = { 1, 2, 3, 4, 5};
        String str[] = {"java", "c", "Python"};
        for(int i=0;i<arr.length;i++)
        {
            System.out.println(arr[i]);
        }
        for(int i=0;i<str.length;i++)
        {
            System.out.println(str[i]);
        }
    }
}